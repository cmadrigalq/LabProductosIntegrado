    private void bttnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnAgregarActionPerformed
        try {
            Producto p = buildProducto();
            control.insert(p);
            control.findProductoByNombre(p.getNombre());
            clearForm();
             JOptionPane.showMessageDialog(null,"Producto insertado exitosamente");
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Por favor, verifique que el precio sea de tipo nÃºmerico");
        }
    }//GEN-LAST:event_bttnAgregarActionPerformed

    private Producto buildProducto() {
        return new Producto(
                this.txtFldCodigo.getText(),
                this.txtFldNombre.getText(),
                Integer.parseInt(this.txtFldPrecio.getText()),
                this.ckboxImportado.isSelected(),
                control.getTipos().get(this.comboTipo.getSelectedIndex())
        );
    }
    
    private void clearForm() {
        this.txtFldCodigo.setText("");
        this.txtFldNombre.setText("");
        this.txtFldPrecio.setText("");
        this.ckboxImportado.setSelected(false);
        this.comboTipo.setSelectedIndex(0);
    }

    
